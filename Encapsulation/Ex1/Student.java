public class Student {
   private String Name;
   private   int Age;

    public void setName(String newName){
        Name = newName;
    }
    public String getName(){
        return Name;
    }

    public void setAge(int age) {
        Age = age;
    }

    public int getAge() {
        return Age;
    }
}
